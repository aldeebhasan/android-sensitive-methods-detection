package android.util;

public class AndroidRuntimeException extends RuntimeException
{
    public AndroidRuntimeException() {
        throw new RuntimeException("Stub!");
    }
    
    public AndroidRuntimeException(final String name) {
        throw new RuntimeException("Stub!");
    }
    
    public AndroidRuntimeException(final String name, final Throwable cause) {
        throw new RuntimeException("Stub!");
    }
    
    public AndroidRuntimeException(final Exception cause) {
        throw new RuntimeException("Stub!");
    }
}
